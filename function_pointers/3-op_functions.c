#include "calc.h"

/**
 * op_add - add operations.
 * @a: int value.
 * @b: int value.
 * Return: equation result.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub operations.
 * @a: int value.
 * @b: int value.
 * Return: equation result.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiple operations.
 * @a: int value.
 * @b: int value.
 * Return: equation result.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division operaions.
 * @a: int value.
 * @b: int value.
 * Return: equation result.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder operations.
 * @a: int value.
 * @b: int value.
 * Return: equation result.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
